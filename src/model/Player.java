package model;

import java.util.GregorianCalendar;

public class Player extends Person  {
	
	Player prior;
	Player next;

	public Player(String id, String firstName, String lastName, String email, String country, char gender, String photo,
			GregorianCalendar birthDate) {
		super(id, firstName, lastName, email, country, gender, photo, birthDate);
		this.prior = null;
		this.next = null;
	}

	public Player getPrior() {
		return prior;
	}

	public void setPrior(Player prior) {
		this.prior = prior;
	}

	public Player getNext() {
		return next;
	}

	public void setNext(Player next) {
		this.next = next;
	}
	
	public String toString() {
		String info = "PLAYER \n \n";
		info += super.toString();
		
		return info;
	}

	
	
	
	
	
	
	

} //end of class
